public class mapInfo{
	int maplength=0;
	int mawidth=0;
	boolean userModel=false;





	public  mapInfo(){
		
		
	}
	
	
	protected int getMaplength() {
		return maplength;
	}

	protected void setMaplength(int maplength) {
		this.maplength = maplength;
	}

	protected int getMawidth() {
		return mawidth;
	}

	protected void setMawidth(int mawidth) {
		this.mawidth = mawidth;
	}
	
	protected boolean isUserModel() {
		return userModel;
	}

	protected void setUserModel(boolean userModel) {
		this.userModel = userModel;
	}
	
}